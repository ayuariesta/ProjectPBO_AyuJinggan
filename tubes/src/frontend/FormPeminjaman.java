    public void tampilkanData() {
        String[] kolom = {"ID Peminjaman", "ID Karyawan", "ID Pengguna", 
            "ID Sepeda", "Tanggal Pinjam", "Tanggal Kembali", "Jumlah Sepeda", "Hari", "Harga", "Total Harga"};
        ArrayList<Peminjaman> list = new Peminjaman().getAll();
        Object rowData[] = new Object[10];

        tabelpinjam.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdpeminjaman();
            rowData[1] = list.get(i).getKaryawan().getIdkaryawan();
            rowData[2] = list.get(i).getPenggunaku().getIdku();
            rowData[3] = list.get(i).getSepeda().getIdsepeda();
            rowData[4] = list.get(i).getWaktupeminjaman();
            rowData[5] = list.get(i).getWaktupengembalian();
            rowData[6] = list.get(i).getJumlahsepeda();
            rowData[7] = list.get(i).getHari();
            rowData[8] = list.get(i).getHarga();
            rowData[9] = list.get(i).getTotal();
            
            ((DefaultTableModel)tabelpinjam.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword) {
        String[] kolom = {"ID Peminjaman", "ID Karyawan", "ID Pengguna", "ID Sepeda", 
            "Tanggal Pinjam", "Tanggal Kembali", "Jumlah Sepeda", "Hari", "Harga", "Total Harga"};
        ArrayList<Peminjaman> list = new Peminjaman().search(keyword);
        Object rowData[] = new Object[10];

        tabelpinjam.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Peminjaman pinjam : list) {
            rowData[0] = pinjam.getIdpeminjaman();
            rowData[1] = pinjam.getKaryawan().getIdkaryawan();
            rowData[2] = pinjam.getPenggunaku().getIdku();
            rowData[3] = pinjam.getSepeda().getIdsepeda();
            rowData[4] = pinjam.getWaktupeminjaman();
            rowData[5] = pinjam.getWaktupengembalian();
            rowData[6] = pinjam.getJumlahsepeda();
            rowData[7] = pinjam.getHari();
            rowData[8] = pinjam.getHarga();
            rowData[9] = pinjam.getTotal();

            ((DefaultTableModel)tabelpinjam.getModel()).addRow(rowData);
        }
    }
    
