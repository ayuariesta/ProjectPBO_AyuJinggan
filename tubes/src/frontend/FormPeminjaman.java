    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        Peminjaman pinjam = new Peminjaman();
        pinjam.setIdpeminjaman(Integer.parseInt(txtpinjam.getText()));
        pinjam.setKaryawan(new Karyawan().getById(Integer.parseInt(txtkaryawan.getText())));
        pinjam.setPenggunaku(new PenggunaKu().getById(Integer.parseInt(txtpengguna.getText())));
        pinjam.setSepeda(new Sepeda().getById(Integer.parseInt(txtsepeda.getText())));
        pinjam.setWaktupeminjaman(txttanggalpinjam.getText());
        pinjam.setWaktupengembalian(txttanggalkembali.getText());
        pinjam.setJumlahsepeda(Integer.parseInt(txtjumlahsepeda.getText()));
        pinjam.setHari(Integer.parseInt(txthari.getText()));
        pinjam.setHarga(Integer.parseInt(txtharga.getText()));
        
        int a = Integer.parseInt(txtjumlahsepeda.getText());
        int b = Integer.parseInt(txthari.getText());
        int c = Integer.parseInt(txtharga.getText());
        int hasil = a*b*c;
        pinjam.setTotal(hasil);
        pinjam.save();
        txtpinjam.setText(Integer.toString(pinjam.getIdpeminjaman()));
        tampilkanData();
    }//GEN-LAST:event_btnsimpanActionPerformed

